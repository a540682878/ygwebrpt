package com.ygg.ygwebrpt.model;

public class Liaojian {
    private Integer id;
    private String liaohao;
    private String tuhao;
    private String danzhong;
    private String chengben;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getLiaohao() {
        return liaohao;
    }

    public void setLiaohao(String liaohao) {
        this.liaohao = liaohao;
    }

    public String getTuhao() {
        return tuhao;
    }

    public void setTuhao(String tuhao) {
        this.tuhao = tuhao;
    }

    public String getDanzhong() {
        return danzhong;
    }

    public void setDanzhong(String danzhong) {
        this.danzhong = danzhong;
    }

    public String getChengben() {
        return chengben;
    }

    public void setChengben(String chengben) {
        this.chengben = chengben;
    }
}
